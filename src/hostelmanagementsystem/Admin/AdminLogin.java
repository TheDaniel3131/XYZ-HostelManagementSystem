    private void cbShowPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbShowPasswordActionPerformed
        String check_pass = new String(pfpPassword.getPassword());
        if (cbShowPassword.isSelected()) {
            pfpPassword.setEchoChar((char) 0);
        } else if (!(cbShowPassword.isSelected()) & (check_pass.equals("") | check_pass.equals("Enter the password..."))) {
            pfpPassword.setEchoChar((char) 0);
        } else {
            pfpPassword.setEchoChar('\u25cf');
        }
    }//GEN-LAST:event_cbShowPasswordActionPerformed

