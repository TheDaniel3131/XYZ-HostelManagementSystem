    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = pfpPassword.getText();
        LocalDateTime now = LocalDateTime.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");

        try {
            File f = new File(FilesLocation.ADMIN_DATABASE);
            Scanner s = new Scanner(f);
            s.useDelimiter("[, \n]");

            // Validate Is Admin Or Not.
            boolean isAdmin = false;

            while (s.hasNext()) {
                String username_db = s.next();
                String password_db = s.next();

                if (username.equals(username_db.trim()) && password.equals(password_db.trim())) {
                    isAdmin = true;
                    JOptionPane.showMessageDialog(null, "Login Success!", "Success", JOptionPane.INFORMATION_MESSAGE);

                    // Record login time
                    FileWriter writer = new FileWriter(FilesLocation.ADMIN_LOGIN_RECORDS);
                    writer.write("Username: " + username + "\n");
                    writer.write("Successfully Authorized As Admin! \n");
                    writer.write("Login Time: " + now.format(formatter) + "\n");
                    writer.close();

                    UserSession userSession = new UserSession(username); // Create UserSession instance
                    AdminMainMenu amm = new AdminMainMenu(userSession); // Pass UserSession instance to AdminMainMenu
                    amm.setVisible(true);
                    this.dispose();

                } else {
                    isAdmin = false;
                    JOptionPane.showMessageDialog(null, "Invalid Input! Please try again.", "Error", JOptionPane.ERROR_MESSAGE);

                    // Record login time
                    FileWriter writer = new FileWriter("admin_login_records.txt", true);
                    writer.write("Username: " + username + "\n");
                    writer.write("Authorization Invalid! \n");
                    writer.write("Login Time: " + now.format(formatter) + "\n");
                    writer.close();
                }

            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException ex) {
            Logger.getLogger(AdminLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

