    void readFile() {
        File formFile = new File(FORMSUB_FILE_PATH);
        if (formFile.exists()) {
            System.err.println("File exists!");
        } else {
            try {
                formFile.createNewFile();
                System.out.println("File created");
            } catch (IOException ex) {
                Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    void importData(String roomID) {
        try ( FileReader fr = new FileReader(ROOM_DETAILS_FILE_PATH);  BufferedReader br = new BufferedReader(fr)) {
            String line;
            String specificColumn1 = "";
            String specificColumn2 = "";

            // Loop through the lines in the text file
            while ((line = br.readLine()) != null) {
                String[] columns = line.split(",");

                String currentRoomID = columns[0];

                if (currentRoomID.equals(roomID)) {
                    specificColumn1 = columns[1].trim(); // Room Type
                    specificColumn2 = columns[4].trim(); // Room Price

                    // Exit the loop since the desired room ID is found
                    break;
                }
            }

            TXT2.setText(specificColumn1);
            TXT1.setText(specificColumn2);

        } catch (IOException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

