    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        ID = J1.getText();
        Course = J2.getText();
        Email = J3.getText();
        Intake = J4.getText();
        Age = J6.getText();
        Address = J5.getText();
        RoomNo = cbRoomNumber.getSelectedItem().toString();
        RoomType = TXT2.getText();
        RoomPrice = TXT1.getText();
        String[] formData = getDataFromForm1();

        readFile();

        if (ID.equals("")) {
            JOptionPane.showMessageDialog(null, "Name is mandatory.");
        } else if (Course.equals("")) {
            JOptionPane.showMessageDialog(null, "Course is mandatory.");
        } else if (Email.equals("")) {
            JOptionPane.showMessageDialog(null, "Email is mandatory.");
        } else if (Intake.equals("")) {
            JOptionPane.showMessageDialog(null, "Intake is mandatory.");
        } else if (Age.equals("")) {
            JOptionPane.showMessageDialog(null, "Age is mandatory.");
        } else if (Address.equals("")) {
            JOptionPane.showMessageDialog(null, "Address is mandatory.");
        } else if (!Age.matches("\\d+")) {
            // Validate the age field using regex to ensure it contains only digits
            JOptionPane.showMessageDialog(null, "Invalid age. Please enter a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!Email.matches("[\\w.]+@[\\w.]+\\.\\w+")) {
            // Validate the email field using regex to ensure it has a valid email format
            JOptionPane.showMessageDialog(null, "Invalid email address. Please enter a valid email.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (Address.length() < 5) {
            // Validate the address field to ensure it has a minimum length of 5 characters
            JOptionPane.showMessageDialog(null, "Invalid address. Please enter a valid address.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!Course.matches("[A-Za-z\\s]+")) {
            // Validate the course field using regex to ensure it contains only alphabets and spaces
            JOptionPane.showMessageDialog(null, "Invalid course name. Please enter a valid course.", "Error", JOptionPane.ERROR_MESSAGE);
            // Validate the course field using regex to ensure it contains only alphabets and spaces
            JOptionPane.showMessageDialog(this, "Invalid course name. Please enter a valid course.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Payment paymentForm = new Payment(formData);
            paymentForm.setVisible(true);
            this.dispose();
            JOptionPane.showMessageDialog(this, "Submitted! Please continue to payment.");
        }
    }//GEN-LAST:event_btnNextActionPerformed

